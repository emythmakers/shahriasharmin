			if (qEl[qElNo].scrollHeight > qEl.innerHeight()) {
				let newElements = document.querySelectorAll('#container .post .column')[qElNo]
				console.log(newElements);
				nextColEl.innerHTML = ''
				removeEl(newElements)
			}
		}
	}
